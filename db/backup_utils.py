def _coerce_sqlite_value_for_postgres_type(value: Any, pg_type: str) -> Any:
    """Coerce simples de tipos SQLite para tipos PostgreSQL sensíveis."""
    if value is None:
        return None

    normalized_type = (pg_type or "").strip().lower()
    if isinstance(value, str) and value.strip() == "":
        if any(token in normalized_type for token in ("timestamp", "date", "time", "bool", "int", "numeric", "decimal", "double", "real")):
            return None
        return value

    if any(token in normalized_type for token in ("timestamp", "date", "time")):
        parsed = _parse_backup_datetime_value(value)
        if parsed is None:
            return None
        if "date" in normalized_type and "time" not in normalized_type:
            return parsed[:10]
        return parsed

    if "bool" not in normalized_type:
        return value

    if isinstance(value, bool):
        return value

    if isinstance(value, (int, float)) and not isinstance(value, bool):
        numeric = int(value)
        if numeric in (0, 1):
            return bool(numeric)
        return value

    text = str(value).strip().lower()
    true_values = {"1", "true", "t", "yes", "y", "sim", "s"}
    false_values = {"0", "false", "f", "no", "n", "nao", "não"}
    if text in true_values:
        return True
    if text in false_values:
        return False

    return value


def _overwrite_postgres_from_sql_file(sql_file: Path) -> tuple[bool, str]:
    db_uri = _build_pg_uri_for_cli()

    try:
        sql_head = sql_file.read_text(encoding="utf-8", errors="ignore")[:4096]
    except Exception:
        sql_head = ""

    is_data_only_dump = "BF1 POSTGRES DATA-ONLY DUMP" in sql_head

    if is_data_only_dump:
        try:
            sql_content = sql_file.read_text(encoding="utf-8", errors="ignore")
            with db_connect() as conn:
                cursor = conn.cursor()
                statements = [s.strip() for s in sql_content.split(";") if s.strip()]
                for statement in statements:
                    cursor.execute(statement)
                _sync_postgres_identity_sequences(cursor)
                conn.commit()
            return True, ""
        except Exception as exc:
            return False, f"Falha ao aplicar dump data-only: {exc}"

    # Para dumps completos, limpar tabelas do schema atual sem depender de ownership do schema.
    try:
        with db_connect() as conn:
            cursor = conn.cursor()
            _drop_all_tables_current_schema(cursor)
            conn.commit()
    except Exception as exc:
        return False, f"Falha ao limpar tabelas atuais: {exc}"

    ok, error = _run_pg_client_command(
        [
            "psql",
            "--dbname",
            db_uri,
            "--set",
            "ON_ERROR_STOP=on",
            "--single-transaction",
            "--file",
            str(sql_file),
        ]
    )
    if not ok:
        return False, f"Falha ao restaurar dump: {error}"

    try:
        with db_connect() as conn:
            cursor = conn.cursor()
            _sync_postgres_identity_sequences(cursor)
            conn.commit()
    except Exception as exc:
        return False, f"Dump restaurado, mas falhou ao sincronizar sequências: {exc}"

    return True, ""


def _migrate_sqlite_file_to_postgres(sqlite_file: Path) -> tuple[bool, str, dict[str, int]]:
    stats = {
        "tables": 0,
        "rows": 0,
        "skipped_rows": 0,
        "skipped_unique_rows": 0,
        "rows_by_table": {},
        "skipped_by_table": {},
        "skipped_unique_by_table": {},
    }
    if not sqlite_file.exists():
        return False, "Arquivo .db não encontrado", stats

    source = sqlite3.connect(str(sqlite_file))
    source.row_factory = sqlite3.Row
    target_ctx = db_connect()

    try:
        with target_ctx as target:
            cursor_t = target.cursor()

            cursor_s = source.cursor()
            cursor_s.execute(
                "SELECT name, sql FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name"
            )
            tables = cursor_s.fetchall()
            table_names = [str(t[0]) for t in tables if t and t[0]]
            known_tables = set(table_names)

            for table in tables:
                table_name = _sanitize_identifier(table[0])
                create_sql = table[1]
                if not create_sql:
                    continue

                # Garante tabela no destino sem depender de ownership de schema.
                ddl = _convert_sqlite_ddl_to_postgres(create_sql)
                ddl = re.sub(r"^\s*CREATE\s+TABLE\s+", "CREATE TABLE IF NOT EXISTS ", ddl, flags=re.IGNORECASE)
                cursor_t.execute(ddl)
                stats["tables"] += 1

            # Ordena carga de dados respeitando dependências de FK (ex.: usuarios/provas antes de apostas).
            deps_map: dict[str, set[str]] = {}
            for table_name in table_names:
                deps_map[table_name] = _get_sqlite_table_fk_dependencies(cursor_s, table_name, known_tables)
            load_order = _topological_sort_tables(table_names, deps_map)

            table_index: dict[str, Any] = {str(t[0]): t for t in tables}

            for table_name_raw in load_order:
                table = table_index.get(table_name_raw)
                if not table:
                    continue
                table_name = _sanitize_identifier(table[0])
                cursor_s.execute(f'SELECT * FROM "{table_name}"')
                rows = cursor_s.fetchall()

                # Limpa dados da tabela de destino por sobrescrita.
                # Primeiro tenta TRUNCATE (mais rápido), com fallback para DELETE.
                try:
                    cursor_t.execute(
                        f"TRUNCATE TABLE {_quote_identifier(table_name)} RESTART IDENTITY CASCADE"
                    )
                except Exception:
                    cursor_t.execute(f"DELETE FROM {_quote_identifier(table_name)}")

                if not rows:
                    continue

                source_cols = [str(col) for col in rows[0].keys()]
                cursor_t.execute(f"PRAGMA table_info('{table_name}')")
                target_cols_info = cursor_t.fetchall() or []
                target_cols = [str(col[1]) for col in target_cols_info if len(col) > 1]
                target_col_types = {
                    str(col[1]): str(col[2]).lower()
                    for col in target_cols_info
                    if len(col) > 2 and col[1]
                }
                cols = [c for c in source_cols if c in target_cols]
                if not cols:
                    continue

                placeholders = ", ".join(["?"] * len(cols))
                cols_sql = ", ".join(_quote_identifier(col) for col in cols)
                insert_sql = (
                    f"INSERT INTO {_quote_identifier(table_name)} ({cols_sql}) "
                    f"VALUES ({placeholders})"
                )
                values = [
                    tuple(
                        _coerce_sqlite_value_for_postgres_type(
                            row[col],
                            target_col_types.get(col, ""),
                        )
                        for col in cols
                    )
                    for row in rows
                ]
                if _is_log_table(table_name):
                    skipped_unique = 0
                else:
                    unique_constraints = _get_postgres_unique_constraints(cursor_t, table_name)
                    values, skipped_unique = _deduplicate_rows_by_unique_constraints(values, cols, unique_constraints)
                    stats["skipped_unique_rows"] += skipped_unique
                    stats["skipped_unique_by_table"][table_name] = int(
                        stats["skipped_unique_by_table"].get(table_name, 0) + skipped_unique
                    )
                if _is_log_table(table_name):
                    values, nullified_fk = _nullify_missing_fk_refs(cursor_t, table_name, cols, values)
                    if nullified_fk > 0:
                        stats["skipped_by_table"][table_name] = int(
                            stats["skipped_by_table"].get(table_name, 0) + nullified_fk
                        )
                else:
                    values, skipped = _filter_rows_with_missing_fk_refs(cursor_t, table_name, cols, values)
                    stats["skipped_rows"] += skipped
                    stats["skipped_by_table"][table_name] = int(
                        stats["skipped_by_table"].get(table_name, 0) + skipped
                    )
                if not values:
                    continue
                cursor_t.executemany(insert_sql, values)
                stats["rows"] += len(values)
                stats["rows_by_table"][table_name] = int(
                    stats["rows_by_table"].get(table_name, 0) + len(values)
                )

            _sync_postgres_identity_sequences(cursor_t)
            target.commit()
        return True, "", stats
    except Exception as exc:
        return False, str(exc), stats
    finally:
        source.close()

