def _parse_backup_datetime_value(value):
    """Normaliza valores de data/hora de importacao sem acionar warnings do pandas."""
    if pd.isna(value):
        return None

    if isinstance(value, datetime):
        return value.strftime('%Y-%m-%d %H:%M:%S')

    raw = str(value).strip()
    if not raw:
        return None

    formatos_explicitos = (
        '%Y-%m-%dT%H:%M:%S.%f%z',
        '%Y-%m-%dT%H:%M:%S%z',
        '%Y-%m-%d %H:%M:%S',
        '%Y-%m-%d',
        '%d/%m/%Y %H:%M:%S',
        '%d/%m/%Y',
    )
    for formato in formatos_explicitos:
        parsed = pd.to_datetime(raw, format=formato, errors='coerce')
        if pd.notna(parsed):
            return parsed.strftime('%Y-%m-%d %H:%M:%S')

    usa_dayfirst = bool(re.match(r'^\d{1,2}[-/]\d{1,2}[-/]\d{4}(?:\s+\d{1,2}:\d{2}(?::\d{2})?)?$', raw))
    parsed = pd.to_datetime(raw, errors='coerce', dayfirst=usa_dayfirst)
    if pd.notna(parsed):
        return parsed.strftime('%Y-%m-%d %H:%M:%S')
    return None


def _require_master_access():
    perfil = st.session_state.get("user_role", "participante")
    if perfil != "master":
        st.warning("Acesso restrito ao usuário master.")
        st.stop()


def _is_postgres_backend() -> bool:
    return DB_BACKEND == "postgres"


def _sanitize_identifier(identifier: str) -> str:
    value = (identifier or "").strip()
    if not re.fullmatch(r"[A-Za-z_][A-Za-z0-9_]*", value):
        raise ValueError(f"Identificador inválido: {identifier}")
    return value


def _quote_identifier(identifier: str) -> str:
    return f'"{_sanitize_identifier(identifier)}"'


def _convert_sqlite_ddl_to_postgres(create_sql: str) -> str:
    ddl = create_sql
    ddl = re.sub(
        r"INTEGER\s+PRIMARY\s+KEY\s+AUTOINCREMENT",
        "INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY",
        ddl,
        flags=re.IGNORECASE,
    )
    ddl = re.sub(r"DEFAULT\s*\(datetime\('now'\)\)", "DEFAULT CURRENT_TIMESTAMP", ddl, flags=re.IGNORECASE)
    ddl = ddl.replace("`", '"')
    return ddl


def _build_pg_uri_for_cli() -> str:
    if DATABASE_URL:
        return DATABASE_URL

    host = os.environ.get("PGHOST", "").strip()
    port = os.environ.get("PGPORT", "5432").strip()
    dbname = os.environ.get("PGDATABASE", "").strip()
    user = os.environ.get("PGUSER", "").strip()
    password = os.environ.get("PGPASSWORD", "").strip()

    if not host or not dbname or not user:
        raise RuntimeError("DATABASE_URL não configurada e variáveis PG* incompletas")

    pwd = quote_plus(password)
    return f"postgresql://{user}:{pwd}@{host}:{port}/{dbname}?sslmode=require"


def _run_pg_client_command(args: list[str], env_overrides: dict[str, str] | None = None) -> tuple[bool, str]:
    env = os.environ.copy()
    if env_overrides:
        env.update(env_overrides)
    try:
        result = subprocess.run(args, capture_output=True, text=True, env=env, check=False)
    except FileNotFoundError:
        return False, f"Comando não encontrado: {args[0]}. Instale 'postgresql-client' no ambiente."

    if result.returncode != 0:
        stderr = (result.stderr or "").strip()
        stdout = (result.stdout or "").strip()
        details = stderr or stdout or "erro desconhecido"
        return False, details
    return True, ""


def _detect_pg_dump_command() -> str | None:
    """Localiza binário pg_dump disponível (com ou sem sufixo de versão)."""
    candidates = ["pg_dump", "pg_dump16", "pg_dump15", "pg_dump14", "pg_dump13", "pg_dump12"]
    for cmd in candidates:
        if shutil.which(cmd):
            return cmd
    return None


def get_postgres_backup_mode() -> tuple[str, str]:
    """Detecta modo de backup disponível para PostgreSQL.

    Returns:
        tuple[str, str]:
            - mode: "full" quando pg_dump completo está disponível
                    "fallback" quando somente data-only interno deve ser usado
            - detail: detalhe técnico resumido para exibir na UI
    """
    if not _is_postgres_backend():
        return "sqlite", ""

    pg_dump_cmd = _detect_pg_dump_command()
    if not pg_dump_cmd:
        return "fallback", "pg_dump não encontrado no ambiente"

    try:
        db_uri = _build_pg_uri_for_cli()
        with tempfile.NamedTemporaryFile(prefix="bf1_pg_probe_", suffix=".sql", delete=False) as tmp:
            probe_path = Path(tmp.name)

        ok, error = _run_pg_client_command(
            [
                pg_dump_cmd,
                "--dbname",
                db_uri,
                "--schema-only",
                "--no-owner",
                "--no-privileges",
                "--file",
                str(probe_path),
            ]
        )

        if probe_path.exists():
            probe_path.unlink(missing_ok=True)

        if ok:
            return "full", f"Compatível com {pg_dump_cmd} local"

        if "server version mismatch" in error.lower():
            return "fallback", "Incompatibilidade de versão do pg_dump"

        if "comando não encontrado" in error.lower():
            return "fallback", "pg_dump não encontrado no ambiente"

        return "fallback", "pg_dump indisponível para backup completo"

    except Exception as exc:
        return "fallback", f"Falha ao validar pg_dump: {exc}"


def _sql_literal(value: Any) -> str:
    if value is None:
        return "NULL"
    if isinstance(value, bool):
        return "TRUE" if value else "FALSE"
    if isinstance(value, (int, float)):
        return str(value)
    if isinstance(value, bytes):
        return "'\\x" + value.hex() + "'::bytea"
    text = str(value).replace("'", "''")
    return f"'{text}'"


def _drop_all_tables_current_schema(cursor: Any) -> None:
    cursor.execute(
        """
        SELECT table_name
        FROM information_schema.tables
        WHERE table_schema = current_schema()
          AND table_type = 'BASE TABLE'
        ORDER BY table_name
        """
    )
    table_rows = cursor.fetchall() or []
    table_names = [str(r[0]) for r in table_rows if r and r[0]]
    for table_name in table_names:
        cursor.execute(f"DROP TABLE IF EXISTS {_quote_identifier(table_name)} CASCADE")


def _list_current_schema_tables(cursor: Any) -> list[str]:
    cursor.execute(
        """
        SELECT table_name
        FROM information_schema.tables
        WHERE table_schema = current_schema()
          AND table_type = 'BASE TABLE'
        ORDER BY table_name
        """
    )
    rows = cursor.fetchall() or []
    return [str(r[0]) for r in rows if r and r[0]]


def _sync_postgres_identity_sequence(cursor: Any, table_name: str) -> None:
    """Ajusta sequência de coluna id para continuar após o maior ID existente."""
    safe_table = _sanitize_identifier(table_name)

    cursor.execute(f"PRAGMA table_info('{safe_table}')")
    cols_info = cursor.fetchall() or []
    cols = [str(c[1]).lower() for c in cols_info if len(c) > 1 and c[1]]
    if "id" not in cols:
        return

    cursor.execute("SELECT pg_get_serial_sequence(%s, %s)", (safe_table, "id"))
    row = cursor.fetchone()
    seq_name = row[0] if row else None
    if not seq_name:
        return

    cursor.execute(
        f"""
        SELECT setval(
            pg_get_serial_sequence(%s, %s),
            COALESCE(MAX({_quote_identifier('id')}), 1),
            MAX({_quote_identifier('id')}) IS NOT NULL
        )
        FROM {_quote_identifier(safe_table)}
        """,
        (safe_table, "id"),
    )


def _sync_postgres_identity_sequences(cursor: Any, table_names: list[str] | None = None) -> None:
    targets = table_names or _list_current_schema_tables(cursor)
    for table_name in targets:
        try:
            _sync_postgres_identity_sequence(cursor, table_name)
        except Exception:
            # Não interrompe fluxo de importação por falha pontual em sequência.
            continue


def _generate_postgres_data_only_dump() -> bytes:
    """Gera dump SQL data-only para fallback quando pg_dump não é compatível."""
    lines: list[str] = []
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    lines.append("-- BF1 POSTGRES DATA-ONLY DUMP")
    lines.append(f"-- gerado em {ts}")

    with db_connect() as conn:
        cursor = conn.cursor()
        cursor.execute(
            """
            SELECT table_name
            FROM information_schema.tables
            WHERE table_schema = current_schema()
              AND table_type = 'BASE TABLE'
            ORDER BY table_name
            """
        )
        table_rows = cursor.fetchall() or []
        table_names = [str(r[0]) for r in table_rows if r and r[0]]

        lines.append("BEGIN;")
        if table_names:
            quoted_tables = ", ".join(_quote_identifier(t) for t in table_names)
            lines.append(f"TRUNCATE TABLE {quoted_tables} RESTART IDENTITY CASCADE;")

        for table_name in table_names:
            cursor.execute(f"PRAGMA table_info('{table_name}')")
            cols_info = cursor.fetchall() or []
            cols = [str(c[1]) for c in cols_info]
            if not cols:
                continue
            cols_sql = ", ".join(_quote_identifier(c) for c in cols)

            cursor.execute(f"SELECT * FROM {_quote_identifier(table_name)}")
            rows = cursor.fetchall() or []
            for row in rows:
                values_sql = ", ".join(_sql_literal(row[i]) for i in range(len(cols)))
                lines.append(
                    f"INSERT INTO {_quote_identifier(table_name)} ({cols_sql}) VALUES ({values_sql});"
                )

        lines.append("COMMIT;")

    return ("\n".join(lines) + "\n").encode("utf-8")


def _get_sqlite_table_fk_dependencies(cursor: sqlite3.Cursor, table_name: str, known_tables: set[str]) -> set[str]:
    deps: set[str] = set()
    try:
        cursor.execute(f'PRAGMA foreign_key_list("{table_name}")')
        rows = cursor.fetchall() or []
        for row in rows:
            # sqlite PRAGMA foreign_key_list -> column "table" na posição 2
            ref_table = str(row[2]).strip() if len(row) > 2 and row[2] else ""
            if ref_table and ref_table in known_tables and ref_table != table_name:
                deps.add(ref_table)
    except Exception:
        return set()
    return deps


def _topological_sort_tables(table_names: list[str], deps_map: dict[str, set[str]]) -> list[str]:
    remaining = set(table_names)
    result: list[str] = []

    while remaining:
        ready = sorted([t for t in remaining if deps_map.get(t, set()).issubset(set(result))])
        if not ready:
            # Em ciclos improváveis, preserva determinismo e segue com o restante.
            result.extend(sorted(remaining))
            break
        for table_name in ready:
            result.append(table_name)
            remaining.remove(table_name)
    return result


def _get_postgres_single_column_fks(cursor: Any, table_name: str) -> list[tuple[str, str, str]]:
    """Retorna FKs simples (1 coluna) da tabela no schema atual."""
    cursor.execute(
        """
        SELECT
            kcu.column_name,
            ccu.table_name AS ref_table,
            ccu.column_name AS ref_column
        FROM information_schema.table_constraints tc
        JOIN information_schema.key_column_usage kcu
          ON tc.constraint_name = kcu.constraint_name
         AND tc.table_schema = kcu.table_schema
        JOIN information_schema.referential_constraints rc
          ON tc.constraint_name = rc.constraint_name
         AND tc.table_schema = rc.constraint_schema
        JOIN information_schema.constraint_column_usage ccu
          ON rc.unique_constraint_name = ccu.constraint_name
         AND rc.unique_constraint_schema = ccu.constraint_schema
        WHERE tc.constraint_type = 'FOREIGN KEY'
          AND tc.table_schema = current_schema()
          AND tc.table_name = %s
        """,
        (table_name,),
    )
    rows = cursor.fetchall() or []
    fks: list[tuple[str, str, str]] = []
    for row in rows:
        if not row or len(row) < 3:
            continue
        col = str(row[0])
        ref_table = str(row[1])
        ref_col = str(row[2])
        if col and ref_table and ref_col:
            fks.append((col, ref_table, ref_col))
    return fks


def _filter_rows_with_missing_fk_refs(
    cursor: Any,
    table_name: str,
    cols: list[str],
    values: list[tuple[Any, ...]],
) -> tuple[list[tuple[Any, ...]], int]:
    """Descarta linhas que referenciam chaves estrangeiras inexistentes no destino."""
    if not values:
        return values, 0

    def _normalize_fk_compare_value(raw: Any) -> Any:
        if raw is None:
            return None
        if isinstance(raw, bool):
            return int(raw)
        if isinstance(raw, int):
            return raw
        if isinstance(raw, float):
            if raw.is_integer():
                return int(raw)
            return raw
        text = str(raw).strip()
        if text == "":
            return None
        if re.fullmatch(r"[+-]?\d+", text):
            try:
                return int(text)
            except Exception:
                return text
        return text

    filtered_values = values
    skipped_total = 0
    fks = _get_postgres_single_column_fks(cursor, table_name)

    for fk_col, ref_table, ref_col in fks:
        if fk_col not in cols:
            continue
        fk_idx = cols.index(fk_col)

        cursor.execute(f"SELECT {_quote_identifier(ref_col)} FROM {_quote_identifier(ref_table)}")
        valid_refs = {
            _normalize_fk_compare_value(row[0])
            for row in (cursor.fetchall() or [])
        }
        if not valid_refs:
            before = len(filtered_values)
            filtered_values = [row for row in filtered_values if row[fk_idx] is None]
            skipped_total += before - len(filtered_values)
            continue

        before = len(filtered_values)
        filtered_values = [
            row
            for row in filtered_values
            if _normalize_fk_compare_value(row[fk_idx]) is None
            or _normalize_fk_compare_value(row[fk_idx]) in valid_refs
        ]
        skipped_total += before - len(filtered_values)

        if not filtered_values:
            break

    return filtered_values, skipped_total


def _nullify_missing_fk_refs(
    cursor: Any,
    table_name: str,
    cols: list[str],
    values: list[tuple[Any, ...]],
) -> tuple[list[tuple[Any, ...]], int]:
    """Mantém linhas, mas converte FK órfã para NULL quando a coluna existe na carga."""
    if not values:
        return values, 0

    adjusted_values = values
    adjusted_cells = 0
    fks = _get_postgres_single_column_fks(cursor, table_name)

    for fk_col, ref_table, ref_col in fks:
        if fk_col not in cols:
            continue
        fk_idx = cols.index(fk_col)
        cursor.execute(f"SELECT {_quote_identifier(ref_col)} FROM {_quote_identifier(ref_table)}")
        valid_refs = {_normalize_unique_key_value(row[0]) for row in (cursor.fetchall() or [])}

        new_values: list[tuple[Any, ...]] = []
        for row in adjusted_values:
            current = _normalize_unique_key_value(row[fk_idx])
            if current is None or current in valid_refs:
                new_values.append(row)
                continue
            row_list = list(row)
            row_list[fk_idx] = None
            new_values.append(tuple(row_list))
            adjusted_cells += 1

        adjusted_values = new_values

    return adjusted_values, adjusted_cells


def _get_postgres_unique_constraints(cursor: Any, table_name: str) -> list[tuple[str, list[str]]]:
    """Retorna constraints UNIQUE/PRIMARY KEY e suas colunas para uma tabela PostgreSQL."""
    cursor.execute(
        """
        SELECT
            c.conname,
            array_agg(a.attname ORDER BY u.ordinality) AS cols
        FROM pg_constraint c
        JOIN pg_class t
          ON t.oid = c.conrelid
        JOIN pg_namespace n
          ON n.oid = t.relnamespace
        JOIN LATERAL unnest(c.conkey) WITH ORDINALITY AS u(attnum, ordinality)
          ON TRUE
        JOIN pg_attribute a
          ON a.attrelid = t.oid
         AND a.attnum = u.attnum
        WHERE n.nspname = current_schema()
          AND t.relname = %s
          AND c.contype IN ('u', 'p')
        GROUP BY c.conname
        ORDER BY c.conname
        """,
        (table_name,),
    )
    rows = cursor.fetchall() or []
    result: list[tuple[str, list[str]]] = []
    for row in rows:
        if not row or len(row) < 2:
            continue
        name = str(row[0])
        cols_raw = row[1] or []
        cols = [str(c) for c in cols_raw if c]
        if cols:
            result.append((name, cols))
    return result


def _normalize_unique_key_value(raw: Any) -> Any:
    if raw is None:
        return None
    if isinstance(raw, bool):
        return int(raw)
    if isinstance(raw, int):
        return raw
    if isinstance(raw, float):
        if raw.is_integer():
            return int(raw)
        return raw
    text = str(raw).strip()
    if text == "":
        return None
    if re.fullmatch(r"[+-]?\d+", text):
        try:
            return int(text)
        except Exception:
            return text
    return text


def _deduplicate_rows_by_unique_constraints(
    values: list[tuple[Any, ...]],
    cols: list[str],
    unique_constraints: list[tuple[str, list[str]]],
) -> tuple[list[tuple[Any, ...]], int]:
    """Remove duplicidades por constraints UNIQUE mantendo a última ocorrência."""
    deduped_values = values
    removed_total = 0

    for _constraint_name, constraint_cols in unique_constraints:
        if not constraint_cols or any(col not in cols for col in constraint_cols):
            continue
        if len(constraint_cols) == 1 and str(constraint_cols[0]).lower() == "id":
            continue

        idxs = [cols.index(col) for col in constraint_cols]
        before = len(deduped_values)
        by_key: dict[tuple[Any, ...], tuple[Any, ...]] = {}

        for row in deduped_values:
            key = tuple(_normalize_unique_key_value(row[idx]) for idx in idxs)
            by_key[key] = row

        deduped_values = list(by_key.values())
        removed_total += before - len(deduped_values)

        if not deduped_values:
            break

    return deduped_values, removed_total


