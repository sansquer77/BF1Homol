def upload_tabela():
    """Interface para upload/importação de tabela específica."""
    _require_master_access()
    tabelas = listar_tabelas()
    
    if not tabelas:
        st.warning("⚠️ Nenhuma tabela encontrada no banco de dados.")
        return
    
    tabela = st.selectbox("Escolha a tabela para sobrescrever:", tabelas, key="select_import")
    
    st.error("🚨 **ATENÇÃO: OPERAÇÃO DESTRUTIVA**")
    st.warning("⚠️ Esta operação irá **DELETAR PERMANENTEMENTE TODOS OS DADOS** da tabela selecionada e substituí-los pelo conteúdo do arquivo Excel. Esta ação não pode ser desfeita!")
    
    uploaded_file = st.file_uploader(
        f"Upload do arquivo .xlsx para substituir dados da tabela '{tabela}'",
        type=["xlsx"], 
        key="upload_one_table"
    )
    
    if uploaded_file is not None and tabela:
        try:
            df = pd.read_excel(uploaded_file)
        except Exception as e:
            st.error(f"❌ Não foi possível ler o Excel: {e}")
            return

        st.write(f"👀 Prévia dos dados ({len(df)} linhas):")
        st.dataframe(df.head(10))

        if st.button("✅ Confirmar Importação - SOBRESCREVER TODOS OS DADOS", type="primary", width="stretch"):
            try:
                table_name = _sanitize_identifier(tabela)
                with db_connect() as conn:
                    cursor = conn.cursor()
                    cols_info = cursor.execute(f"PRAGMA table_info('{table_name}')").fetchall()
                    if not cols_info:
                        raise ValueError(f"Tabela '{table_name}' não encontrada no banco.")
                    db_cols = [r[1] for r in cols_info]
                    db_col_types = {r[1]: (r[2] or '').upper() for r in cols_info}
                    db_col_meta = {
                        r[1]: {
                            "notnull": int(r[3]) if len(r) > 3 and r[3] is not None else 0,
                            "default": r[4] if len(r) > 4 else None,
                            "pk": int(r[5]) if len(r) > 5 and r[5] is not None else 0,
                        }
                        for r in cols_info
                    }

                    missing_cols = [c for c in db_cols if c not in df.columns]
                    extra_cols = [c for c in df.columns if c not in db_cols]

                    required_missing_cols: list[str] = []
                    optional_missing_cols: list[str] = []
                    for col in missing_cols:
                        meta = db_col_meta.get(col, {})
                        is_required = bool(meta.get("notnull")) and meta.get("default") is None and not bool(meta.get("pk"))
                        if is_required:
                            required_missing_cols.append(col)
                        else:
                            optional_missing_cols.append(col)

                    if required_missing_cols:
                        raise ValueError(f"Colunas obrigatórias faltantes no Excel: {required_missing_cols}")
                    if extra_cols:
                        st.info(f"ℹ️ Colunas extras no Excel serão ignoradas: {extra_cols}")

                    if optional_missing_cols:
                        st.info(
                            f"ℹ️ Colunas ausentes opcionais serão preenchidas com vazio/NULL: {optional_missing_cols}"
                        )
                        for col in optional_missing_cols:
                            df[col] = None

                    df_alinhado = df[db_cols].copy()

                    for col_name in db_cols:
                        col_type = db_col_types.get(col_name, '')
                        series = df_alinhado[col_name]

                        if "INT" in col_type or "BOOL" in col_type:
                            coerced = pd.to_numeric(series, errors='coerce').astype('Int64')
                            df_alinhado[col_name] = coerced.where(~coerced.isna(), None)
                            continue
                        if any(t in col_type for t in ["REAL", "FLOA", "DOUB", "NUM", "DEC"]):
                            coerced = pd.to_numeric(series, errors='coerce')
                            df_alinhado[col_name] = coerced.where(~coerced.isna(), None)
                            continue
                        if any(t in col_type for t in ["DATE", "TIME"]):
                            df_alinhado[col_name] = series.apply(_parse_backup_datetime_value)
                            continue

                    df_alinhado = df_alinhado.where(pd.notnull(df_alinhado), None)

                    if _is_postgres_backend():
                        unique_constraints = _get_postgres_unique_constraints(cursor, table_name)
                        duplicate_reports: list[pd.DataFrame] = []
                        for constraint_name, constraint_cols in unique_constraints:
                            if not constraint_cols or any(col not in df_alinhado.columns for col in constraint_cols):
                                continue
                            dup_mask = df_alinhado.duplicated(subset=constraint_cols, keep=False)
                            if not dup_mask.any():
                                continue
                            dup_groups = (
                                df_alinhado.loc[dup_mask, constraint_cols]
                                .groupby(constraint_cols, dropna=False)
                                .size()
                                .reset_index(name="qtd_repeticoes")
                                .sort_values("qtd_repeticoes", ascending=False)
                            )
                            if dup_groups.empty:
                                continue
                            dup_groups.insert(0, "constraint", constraint_name)
                            duplicate_reports.append(dup_groups.head(10))

                        if duplicate_reports:
                            st.warning(
                                "⚠️ Foram detectadas chaves duplicadas no Excel para constraints únicas. "
                                "As linhas serão deduplicadas (mantendo a ocorrência mais recente)."
                            )
                            st.dataframe(pd.concat(duplicate_reports, ignore_index=True), width="stretch")

                        if not _is_log_table(table_name):
                            records_before_dedupe = len(df_alinhado)
                            values_for_dedupe = [
                                tuple(row[col] for col in db_cols)
                                for row in df_alinhado.to_dict("records")
                            ]
                            deduped_values, removed_unique = _deduplicate_rows_by_unique_constraints(
                                values_for_dedupe,
                                db_cols,
                                unique_constraints,
                            )
                            if removed_unique > 0:
                                st.info(
                                    f"ℹ️ {removed_unique} linha(s) duplicada(s) removida(s) antes da inserção "
                                    "por constraints UNIQUE."
                                )
                            if records_before_dedupe != len(deduped_values):
                                df_alinhado = pd.DataFrame(deduped_values, columns=db_cols)

                    count_before = cursor.execute(
                        f"SELECT COUNT(*) FROM {_quote_identifier(table_name)}"
                    ).fetchone()[0]

                    input_rows_count = int(len(df_alinhado))
                    skipped_fk_rows = 0
                    values: list[tuple[Any, ...]] = []

                    nullified_fk_cells = 0

                    if not df_alinhado.empty:
                        values = [
                            tuple(row[col] for col in db_cols)
                            for row in df_alinhado.to_dict("records")
                        ]

                        if _is_postgres_backend():
                            if _is_log_table(table_name):
                                values, nullified_fk_cells = _nullify_missing_fk_refs(
                                    cursor,
                                    table_name,
                                    db_cols,
                                    values,
                                )
                            else:
                                values, skipped_fk_rows = _filter_rows_with_missing_fk_refs(
                                    cursor,
                                    table_name,
                                    db_cols,
                                    values,
                                )

                        # Evita operação destrutiva quando todas as linhas do Excel são órfãs.
                        if (
                            _is_postgres_backend()
                            and not _is_log_table(table_name)
                            and input_rows_count > 0
                            and len(values) == 0
                            and skipped_fk_rows >= input_rows_count
                        ):
                            raise ValueError(
                                "Nenhuma linha válida para importar: todas as linhas referenciam "
                                "FK inexistente. Importe primeiro as tabelas pai (usuarios/provas)."
                            )

                    if _is_postgres_backend():
                        cursor.execute(f"TRUNCATE TABLE {_quote_identifier(table_name)} RESTART IDENTITY CASCADE")
                    else:
                        cursor.execute(f"DELETE FROM {_quote_identifier(table_name)}")

                    count_after_delete = cursor.execute(
                        f"SELECT COUNT(*) FROM {_quote_identifier(table_name)}"
                    ).fetchone()[0]

                    if values:
                        insert_cols = ", ".join(_quote_identifier(col) for col in db_cols)
                        placeholders = ", ".join(["?"] * len(db_cols))
                        insert_sql = (
                            f"INSERT INTO {_quote_identifier(table_name)} ({insert_cols}) "
                            f"VALUES ({placeholders})"
                        )
                        if _is_postgres_backend():
                            insert_sql += " ON CONFLICT DO NOTHING"
                        cursor.executemany(insert_sql, values)

                    if _is_postgres_backend():
                        _sync_postgres_identity_sequences(cursor, [table_name])

                    conn.commit()

                    count_after_insert = cursor.execute(
                        f"SELECT COUNT(*) FROM {_quote_identifier(table_name)}"
                    ).fetchone()[0]
                
                # CRÍTICO: Fechar pool para forçar recriação após modificar dados
                from db.connection_pool import close_pool
                try:
                    close_pool()  # Fecha pool atual, será recriado automaticamente na próxima consulta
                except Exception as e:
                    st.error(f"❌ Erro ao fechar pool de conexões: {e}")
                    raise

                st.success(f"✅ Tabela '{table_name}' completamente sobrescrita!")
                st.info(f"🗑️ Registros deletados: {count_before}")
                st.info(f"📥 Registros importados: {count_after_insert}")
                if skipped_fk_rows > 0:
                    st.warning(
                        f"⚠️ {skipped_fk_rows} linha(s) foram ignoradas por referência inválida "
                        "(chave estrangeira sem linha pai)."
                    )
                if nullified_fk_cells > 0:
                    st.warning(
                        f"⚠️ {nullified_fk_cells} referência(s) de FK órfã em tabela de log "
                        "foram convertidas para NULL para preservar o histórico."
                    )
                skipped_by_conflict = max(
                    0,
                    int(input_rows_count or 0) - int(skipped_fk_rows or 0) - int(count_after_insert or 0),
                )
                if skipped_by_conflict > 0 and _is_postgres_backend():
                    st.warning(
                        f"⚠️ {skipped_by_conflict} linha(s) duplicada(s) foram ignoradas por conflito de chave única."
                    )
                st.cache_data.clear()
                st.rerun()
            except Exception as e:
                st.error(f"❌ Erro ao importar tabela: {e}")
                st.info("💡 Verifique se as colunas do arquivo Excel correspondem exatamente às colunas da tabela.")
                import traceback
                st.code(traceback.format_exc())


