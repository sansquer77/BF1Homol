def excluir_regra(regra_id: int) -> bool:
    """Exclui uma regra se não estiver vinculada a nenhuma temporada"""
    try:
        with get_pool().get_connection() as conn:
            c = conn.cursor()
            c.execute('SELECT COUNT(*) FROM temporadas_regras WHERE regra_id = %s', (regra_id,))
            vinculacoes = c.fetchone()[0]
            if vinculacoes > 0:
                return False
            c.execute('DELETE FROM regras WHERE id = %s', (regra_id,))
            conn.commit()
            logger.info("✓ Regra ID %s excluída", regra_id)
            return True
    except Exception as e:
        logger.exception("Erro ao excluir regra: %s", e)
        return False

def get_regra_por_id(regra_id: int) -> Optional[dict]:
    """Retorna uma regra pelo ID"""
    try:
        with get_pool().get_connection() as conn:
            c = conn.cursor()
            c.execute('SELECT * FROM regras WHERE id = %s', (regra_id,))
            row = c.fetchone()
            return dict(row) if row else None
    except Exception as e:
        logger.exception("Erro ao buscar regra por ID: %s", e)
        return None

