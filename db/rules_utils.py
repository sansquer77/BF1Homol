def get_temporadas_da_regra(regra_id: int) -> list[str]:
    """Retorna temporadas vinculadas a uma regra"""
    try:
        with get_pool().get_connection() as conn:
            c = conn.cursor()
            c.execute('SELECT temporada FROM temporadas_regras WHERE regra_id = %s', (regra_id,))
            rows = c.fetchall()
            return [str(r[0]) for r in rows] if rows else []
    except Exception as e:
        logger.exception("Erro ao buscar temporadas da regra: %s", e)
        return []

def clonar_regra(regra_id: int, novo_nome: str) -> Optional[int]:
    """Clona uma regra existente com novo nome"""
    regra = get_regra_por_id(regra_id)
    if not regra:
        return None
    regra.pop('id', None)
    regra['nome_regra'] = novo_nome
    return salvar_regra(novo_nome, regra)

def vincular_regra_temporada(regra_id: int, temporada: int) -> bool:
    """Vincula uma regra a uma temporada"""
    try:
        with get_pool().get_connection() as conn:
            c = conn.cursor()
            c.execute('''
                INSERT INTO temporadas_regras (regra_id, temporada)
                VALUES (%s, %s)
                ON CONFLICT (temporada) DO UPDATE SET
                    regra_id = EXCLUDED.regra_id
            ''', (regra_id, temporada))
            conn.commit()
            logger.info("✓ Regra %s vinculada à temporada %s", regra_id, temporada)
            return True
    except Exception as e:
        logger.exception("Erro ao vincular regra à temporada: %s", e)
        return False

def get_regras_df():
    """Retorna DataFrame com regras e temporadas vinculadas"""
