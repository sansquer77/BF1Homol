def admin_add_prova(*, nome: str, data: str, horario_prova: str, status: str, tipo: str, temporada: str, circuit_id: str | None = None) -> bool:
    require_operation("prova.write", season=temporada)
    with db_connect() as conn:
        cursor = conn.cursor()
        cursor.execute("SELECT 1 FROM provas WHERE nome=%s AND data=%s AND temporada=%s LIMIT 1", (nome, data, temporada))
        if cursor.fetchone() is not None:
            return False
    return add_prova(nome, data, horario_prova, tipo, status, temporada, circuit_id=circuit_id)


def admin_update_prova(prova_id: int, *, temporada: str, **campos) -> bool:
    require_operation("prova.write", season=temporada)
    with db_connect() as conn:
        if "circuit_id" not in get_table_columns(conn, "provas"):
            campos.pop("circuit_id", None)
    campos["temporada"] = temporada
    return update_prova(prova_id, **campos)


def admin_delete_prova(prova_id: int, temporada: str) -> bool:
    require_operation("prova.write", season=temporada)
    return delete_prova(prova_id)


def admin_save_resultado(prova_id: int, temporada: str, posicoes: dict, abandonos: list[str]) -> None:
    require_operation("resultado.write", season=temporada)
    with db_connect() as conn:
        cols = get_table_columns(conn, "resultados")
        fields = ["prova_id", "posicoes"]
        values: list[object] = [int(prova_id), str(posicoes)]
        updates = ["posicoes = EXCLUDED.posicoes"]
        if "abandono_pilotos" in cols:
            fields.append("abandono_pilotos"); values.append(",".join(abandonos)); updates.append("abandono_pilotos = EXCLUDED.abandono_pilotos")
        if "temporada" in cols:
            fields.append("temporada"); values.append(str(temporada)); updates.append("temporada = EXCLUDED.temporada")
        placeholders = ", ".join(["%s"] * len(fields))
        conn.cursor().execute(f"INSERT INTO resultados ({', '.join(fields)}) VALUES ({placeholders}) ON CONFLICT (prova_id) DO UPDATE SET {', '.join(updates)}", tuple(values))
        conn.commit()
    clear_data_cache()


