def master_delete_usuario(user_id: int) -> bool:
    context = require_operation("usuario.write")
    if int(user_id) == context.user_id:
        raise ValueError("O master nao pode excluir a propria conta.")
    return delete_usuario(int(user_id))


def master_save_hall(entries: Iterable[tuple[int, int, float, str]]) -> int:
    require_operation("hall_da_fama.write")
    values = [(int(u), int(p), float(points), str(season)) for u, p, points, season in entries]
    if not values:
        return 0
    with db_connect() as conn:
        conn.cursor().executemany("""INSERT INTO hall_da_fama (usuario_id,posicao_final,pontos,temporada) VALUES (%s,%s,%s,%s) ON CONFLICT (usuario_id,temporada) DO UPDATE SET posicao_final=EXCLUDED.posicao_final,pontos=EXCLUDED.pontos""", values)
        conn.commit()
    clear_data_cache()
    return len(values)


def master_delete_hall(record_id: int) -> None:
    require_operation("hall_da_fama.write")
    with db_connect() as conn:
        conn.cursor().execute("DELETE FROM hall_da_fama WHERE id=%s", (int(record_id),))
        conn.commit()
    clear_data_cache()


def master_save_financial(temporada: str, pagamentos: dict[int, bool]) -> None:
    require_operation("usuario.write", season=temporada)
    with db_connect() as conn:
        conn.cursor().executemany("""INSERT INTO financeiro_participantes (usuario_id,temporada,pago,atualizado_em) VALUES (%s,%s,%s,CURRENT_TIMESTAMP) ON CONFLICT(usuario_id,temporada) DO UPDATE SET pago=EXCLUDED.pago,atualizado_em=CURRENT_TIMESTAMP""", [(int(uid), str(temporada), 1 if paid else 0) for uid, paid in pagamentos.items()])
        conn.commit()


def master_save_fee(temporada: str, valor: float) -> None:
    require_operation("usuario.write", season=temporada)
    with db_connect() as conn:
        conn.cursor().execute("""INSERT INTO financeiro_config_temporada (temporada,valor_taxa,atualizado_em) VALUES (%s,%s,CURRENT_TIMESTAMP) ON CONFLICT(temporada) DO UPDATE SET valor_taxa=EXCLUDED.valor_taxa,atualizado_em=CURRENT_TIMESTAMP""", (str(temporada), float(valor)))
        conn.commit()
