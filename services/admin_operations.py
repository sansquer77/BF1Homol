def master_ensure_financial_schema() -> None:
    require_operation("usuario.write")
    with db_connect() as conn:
        cursor = conn.cursor()
        cursor.execute("""CREATE TABLE IF NOT EXISTS financeiro_participantes (id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, usuario_id INTEGER NOT NULL REFERENCES usuarios(id), temporada TEXT NOT NULL, pago INTEGER NOT NULL DEFAULT 0, atualizado_em TEXT DEFAULT CURRENT_TIMESTAMP, UNIQUE(usuario_id,temporada))""")
        cursor.execute("""CREATE TABLE IF NOT EXISTS financeiro_config_temporada (id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, temporada TEXT NOT NULL UNIQUE, valor_taxa REAL NOT NULL DEFAULT 0, atualizado_em TEXT DEFAULT CURRENT_TIMESTAMP)""")
        conn.commit()


